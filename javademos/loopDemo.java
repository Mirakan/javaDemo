package javademos;

public class loopDemo {

	public static void main(String[] args) {
		//For
		for (int i = 1; i <10; i++) {
			System.out.println(i);
		}
		System.out.println("Döngü Bitti.");
		
		//While
		int i=1;
		
		while (i<10) {
			System.out.println(i);
			i++;
		}
		System.out.println("Döngü Bitti.");
	}

}
